#!/usr/bin/env bash
# Install Agent Brain Claude Code plugin (hosted SSE + hooks + skill).
#
# Usage:
#   ./plugins/claude-code/install.sh \
#     --url https://agent-memory.nighthawklabs.org/sse \
#     --agent-id claude-you \
#     --api-key YOUR_KEY
#
# Remote (no clone):
#   curl -fsSL https://raw.githubusercontent.com/realnighthawk/agent-plugins/main/plugins/claude-code/install.sh | bash -s -- --api-key ...
set -euo pipefail

_script_path="${BASH_SOURCE[0]:-}"
ROOT=""
if [[ -n "${_script_path}" ]]; then
  _lib="$(cd "$(dirname "${_script_path}")/../.." 2>/dev/null && pwd)/scripts/lib/install-repo.sh"
  if [[ -f "${_lib}" ]]; then
    # shellcheck source=../../scripts/lib/install-repo.sh
    . "${_lib}"
    ROOT="$(plugins_repo_root "${_script_path}")"
  fi
fi
if [[ -z "${ROOT}" ]]; then
  _dest="${AGENT_PLUGINS_INSTALL_DIR:-${HOME}/.local/share/agent-plugins}"
  _repo="${AGENT_PLUGINS_REPO:-https://github.com/realnighthawk/agent-plugins.git}"
  _ref="${AGENT_PLUGINS_REF:-main}"
  if [[ ! -f "${_dest}/go.mod" ]]; then
    echo "agent-plugins: cloning to ${_dest} ..." >&2
    mkdir -p "$(dirname "${_dest}")"
    git clone --depth 1 --branch "${_ref}" "${_repo}" "${_dest}"
  fi
  ROOT="${_dest}"
fi
PLUGIN_DIR="${ROOT}/plugins/claude-code"

MCP_URL="https://agent-memory.nighthawklabs.org/sse"
AGENT_ID=""
API_KEY="${NIGHTHAWK_API_KEY:-}"
JWT="${NIGHTHAWK_JWT:-}"
USER_TAG="${USER:-you}"
SCOPE="user"
MCP_CALL_VERSION="${MCP_CALL_VERSION:-latest}"
SKIP_PLUGIN_ADD=0

usage() {
  sed -n '2,11p' "$SCRIPT_PATH" | sed 's/^# \{0,1\}//'
  exit "${1:-0}"
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --url) MCP_URL="$2"; shift 2 ;;
    --agent-id) AGENT_ID="$2"; shift 2 ;;
    --api-key) API_KEY="$2"; shift 2 ;;
    --jwt) JWT="$2"; shift 2 ;;
    --user) USER_TAG="$2"; shift 2 ;;
    --scope) SCOPE="$2"; shift 2 ;;
    --version) MCP_CALL_VERSION="$2"; shift 2 ;;
    --skip-plugin-add) SKIP_PLUGIN_ADD=1; shift ;;
    -h|--help) usage 0 ;;
    *) echo "unknown arg: $1" >&2; usage 1 ;;
  esac
done

if [[ -z "$AGENT_ID" ]]; then
  if [[ "$SCOPE" == "project" ]]; then
    AGENT_ID="claude-${USER_TAG}-$(basename "$ROOT")"
  else
    AGENT_ID="claude-${USER_TAG}"
  fi
fi

if [[ -z "$API_KEY" && -z "$JWT" ]]; then
  echo "Provide --api-key or --jwt (or set NIGHTHAWK_API_KEY / NIGHTHAWK_JWT)." >&2
  exit 1
fi

ENV_DIR="${HOME}/.config/agent-brain"
ENV_FILE="${ENV_DIR}/claude.env"
mkdir -p "$ENV_DIR"

echo "Installing mcp-call..."
export MCP_CALL_VERSION
"${ROOT}/scripts/fetch-mcp-call.sh" "${PLUGIN_DIR}/bin/mcp-call"

cat >"$ENV_FILE" <<EOF
# Agent Brain Claude Code — generated by install.sh
export NIGHTHAWK_MCP_URL=${MCP_URL}
export NIGHTHAWK_AGENT_ID=${AGENT_ID}
EOF
chmod 600 "$ENV_FILE"

if [[ -n "$JWT" ]]; then
  echo "export NIGHTHAWK_JWT=${JWT}" >>"$ENV_FILE"
  cp "${PLUGIN_DIR}/templates/mcp-jwt.json" "${PLUGIN_DIR}/.mcp.json"
else
  echo "export NIGHTHAWK_API_KEY=${API_KEY}" >>"$ENV_FILE"
fi

register_claude_plugin() {
  local marketplace="${ROOT}/.claude-plugin/marketplace.json"
  if [[ ! -f "${marketplace}" ]]; then
    echo "Missing ${marketplace} — cannot register with Claude Code." >&2
    return 1
  fi

  echo "Registering marketplace with Claude Code..."
  if ! claude plugin marketplace add "${ROOT}" --scope user 2>&1; then
    echo "Note: marketplace may already be registered (agent-plugins)." >&2
  fi

  echo "Installing agent-brain plugin..."
  claude plugin install agent-brain@agent-plugins --scope user
}

if [[ "$SKIP_PLUGIN_ADD" -eq 0 ]]; then
  if command -v claude >/dev/null 2>&1; then
    register_claude_plugin || {
      echo "Plugin registration failed. Install manually:" >&2
      echo "  claude plugin marketplace add ${ROOT} --scope user" >&2
      echo "  claude plugin install agent-brain@agent-plugins --scope user" >&2
    }
  else
    echo "Claude CLI not on PATH — install Claude Code, then run:" >&2
    echo "  claude plugin marketplace add ${ROOT} --scope user" >&2
    echo "  claude plugin install agent-brain@agent-plugins --scope user" >&2
  fi
fi

echo ""
echo "Installed Agent Brain for Claude Code"
echo "  Plugin path: ${PLUGIN_DIR}"
echo "  MCP URL:     ${MCP_URL}"
echo "  Agent ID:    ${AGENT_ID}"
echo "  Env file:    ${ENV_FILE}"
echo ""
echo "Add to your shell profile (~/.zshrc or ~/.bashrc):"
echo "  source ${ENV_FILE}"
echo ""
echo "Server checklist:"
echo "  1. Memory Explorer → Settings: register agent \"${AGENT_ID}\""
echo "  2. Restart Claude Code or run /reload-plugins"
echo ""
echo "Smoke test (after sourcing env):"
echo "  ${PLUGIN_DIR}/bin/mcp-call memory_system_status '{}'"
